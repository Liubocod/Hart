 int TableSearch(float x,int N)  
 {  
     int left=0,right=N-1,mid;
     if(x > FFT_LOG_TABLE[N-1][0])
        return -2;                      //²éÕÒÊý¾Ý³¬·¶Î§
     while(left<=right)
    {
        mid = (left+right)/2;
        if(left == right)
            return  mid;                //²éÕÒ³É¹¦·µ»Ø¾Í·µ»ØË÷Òý
        if(x == FFT_LOG_TABLE[mid][0]) 
            return  mid;                //²éÕÒ³É¹¦·µ»Ø¾Í·µ»ØË÷Òý
        else if(x < FFT_LOG_TABLE[mid][0])
        {
            if(x > FFT_LOG_TABLE[mid-1][0])
                return mid;
            else
                right = mid-1;
        }
        else 
        {
            if(x < FFT_LOG_TABLE[mid+1][0])
                return mid;
            else
                left = mid+1;
        }

     }
     return -1;                         //²éÕÒÊ§°Ü
 }
